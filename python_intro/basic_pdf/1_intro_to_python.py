# # What is python module

# In Python, a module is a file containing Python definitions and statements.
# Definitions are used to create new objects, such as functions, classes, and variables. 
# For example, you can define a function using the def keyword
# Statements are used to perform actions, such as assignments, control flow, and function calls. For example, you can assign a value to a variable using the assignment operator =:
# A python module can be divided into different files and then all these files can be imported into single file to make a module

# # Types of Module in python
# There are two types of module in python:

   #  Predefinded Module: 
   #      These are modules that are included with Python and provide a wide range of functionality, from file I/O 
    #     to data analysis. Examples of built-in modules include os, sys, math, random, etc. You can use these 
    #     modules by importing them into your Python script.
    # User-defined modules: 
    #     These are modules that you create yourself and can contain definitions, statements, and other elements 
    #     that you want to reuse in multiple projects. You can create a user-defined module by saving a Python 
    #     file with a .py extension and then importing it into another Python script.
    # Third-party module:
    #     Third-Party module are created and maintained by other developers and can be installed using a package 
    #     managerlike pip. These modules provide additional functionality that is not included with Python and can
    #     be easily integrated into your projects.


