# Integer data type
num1 = 10
num2 = 5

# Addition
sum = num1 + num2
print("Sum:", sum)

# Subtraction
difference = num1 - num2
print("Difference:", difference)

# Multiplication
product = num1 * num2
print("Product:", product)

# Division
quotient = num1 / num2
print("Quotient:", quotient)

# Modulo
remainder = num1 % num2
print("Remainder:", remainder)

# Float data type
float1 = 3.14
float2 = 2.71

# Addition
float_sum = float1 + float2
print("Float sum:", float_sum)

# Boolean data type
is_true = True
is_false = False

# Comparison
print("10 > 5 is", 10 > 5)
print("10 < 5 is", 10 < 5)
print("10 == 5 is", 10 == 5)
print("10 != 5 is", 10 != 5)

# String data type
string1 = "Hello"
string2 = "world"

# Concatenation
concatenated_string = string1 + " " + string2
print("Concatenated string:", concatenated_string)

# List data type
my_list = [1, 2, 3, 4, 5]

# Accessing elements by index
print("Third element:", my_list[2])

# Slicing
print("Sliced list:", my_list[1:4])

# Changing elements by index
my_list[0] = 0
print("Modified list:", my_list)

# Dictionary data type
my_dict = {'name': 'John', 'age': 30, 'city': 'New York'}

# Accessing values by key
print("Name:", my_dict['name'])
print("Age:", my_dict['age'])
print("City:", my_dict['city'])

# Adding a new key-value pair
my_dict['country'] = 'USA'
print("Modified dictionary:", my_dict)

